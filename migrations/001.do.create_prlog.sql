CREATE TABLE users (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_name TEXT NOT NULL,
    password TEXT NOT NULL,
    date_created TIMESTAMP DEFAULT now() NOT NULL
);

CREATE TABLE goals (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id INTEGER REFERENCES users(id) ON DELETE CASCADE NULL,
    num_of_days INTEGER NOT NULL,
    total_hours INTEGER NOT NULL, 
    hours_goal INTEGER NOT NULL 
);

CREATE TABLE days (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    day_num INTEGER NOT NULL,
    day_date TIMESTAMP DEFAULT now() NOT NULL,
    completed TEXT NOT NULL,
    technique TEXT NOT NULL,
    repertoire TEXT NOT NULL,
    actual_hours DECIMAL(4,2) NOT NULL,
    touched BOOLEAN,
    goal_id INTEGER REFERENCES goals(id) ON DELETE CASCADE NULL,
    user_id INTEGER REFERENCES users(id) ON DELETE CASCADE NULL   
); 